
public class three {
	
	private int a ,b;
	public three (int a ,int b) {
		this.a = a;
		this.b = b;
	}
	
	public int getEqualbyPlus() {
		return this.a + this.b;
	}
	
	public int getEqualbyMinus() {
		return this.a - this.b;
	}
	
	public int getEqualbyMul() {
		return this.a * this.b;
	}
	
	public int getEqualbyDiv() {
		return this.a / this.b;
	}
	
}
